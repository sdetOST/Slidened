package BasicLoops;

public class PrimativeDataTypes {

	public static void main(String[] args) {
		 
		int a = 123, b = 456;
		System.out.println(a);			// 123
		System.out.println(b);			// 456
		
		double d, e, f; 
		d = e = f = 10.55;
		System.out.println(d);			// 10.55
		System.out.println(e);			// 10.55
		System.out.println(f);			// 10.55
		
		boolean red, blue=true, black;
		
//		System.out.println(red);		// ?!  error
		System.out.println(blue);		// true
//		System.out.println(black);		// ?! error
		
		boolean hot, cold, warm = true;
		hot = cold = warm;					// !!!!!!!!!!!!!!!!!!
		System.out.println(hot);			// true
		System.out.println(cold);			// true
		System.out.println(warm);			// true
		
		
		
		
				
		
	
	}

}
